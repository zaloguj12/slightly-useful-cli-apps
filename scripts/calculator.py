import math

def add(num1, num2):
    result = int(num1) + int(num2)
    return result

def min(num1, num2):
    result = int(num1) - int(num2)
    return result

def mult(num1, num2):
    result = int(num1) * int(num2)
    return result

def div(num1, num2):
    result = int(num1) / int(num2)
    return result